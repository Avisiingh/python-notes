# Defining variables
x = 3.14
y = -4
z = 5

# Rounding off x
result = round(x)
print("Rounded value of x:", result)  # Output: 3

# Absolute value of y
result = abs(y)
print("Absolute value of y:", result)  # Output: 4

# Power function (4^3)
result = pow(4, 3)
print("4 raised to the power of 3:", result)  # Output: 64

# Maximum value among x, y, and z
result = max(x, y, z)
print("Maximum value among x, y, and z:", result)  # Output: 5

# Minimum value among x, y, and z
result = min(x, y, z)
print("Minimum value among x, y, and z:", result)  # Output: -4
