import math

# Printing mathematical constants
print("Value of pi:", math.pi)  # Output: 3.141592653589793
print("Value of e:", math.e)    # Output: 2.718281828459045

# Defining a number
x = 9

# Square root of x
result = math.sqrt(x)
print("Square root of x:", result)  # Output: 3.0

# Ceiling function (ceil) - rounds UP to the nearest integer
result = math.ceil(x)
print("Ceiling of x:", result)  # Output: 9

# Floor function - rounds DOWN to the nearest integer
result = math.floor(x)
print("Floor of x:", result)  # Output: 9
