# Taking user input for name and age
name = input("Enter your name: ")  
age = int(input("Enter your age: "))  # Converting input to integer

# Incrementing age
age += 1  

# Displaying output
print(f"\nHello, {name}!")  
print(f"You will be {age} years old next year.")

