# Initial value
friends = 5  
print("Initial friends:", friends)  # Output: Initial friends: 5

# Increment by 1
friends = friends + 1  
print("After increment (friends + 1):", friends)  # Output: 6

# Using shorthand operator for increment
friends += 1  
print("After shorthand increment (friends += 1):", friends)  # Output: 7

# Decrement by 2
friends -= 2  
print("After decrement (friends -= 2):", friends)  # Output: 5

# Multiply by 3
friends = friends * 3  
print("After multiplication (friends * 3):", friends)  # Output: 15

# Using shorthand for multiplication
friends *= 3  
print("After shorthand multiplication (friends *= 3):", friends)  # Output: 45

# Divide by 2
friends = friends / 2  
print("After division (friends / 2):", friends)  # Output: 22.5

# Using shorthand for division
friends /= 2  
print("After shorthand division (friends /= 2):", friends)  # Output: 11.25

# Exponentiation (square)
friends = friends ** 2  
print("After exponentiation (friends ** 2):", friends)  # Output: 126.5625

# Using shorthand for exponentiation
friends **= 2  
print("After shorthand exponentiation (friends **= 2):", friends)  # Output: 16026.55029296875

# Modulus operation
remainder = friends % 5  
print("Remainder when friends is divided by 5:", remainder)  # Output: 1.55029296875

# Working with numerical functions
x = 3.14
y = -4
z = 5

# Rounding off x
result = round(x)
print("Rounded value of x:", result)  # Output: 3

# Absolute value of y
result = abs(y)
print("Absolute value of y:", result)  # Output: 4

# Power function (4^3)
result = pow(4, 3)
print("4 raised to the power of 3:", result)  # Output: 64

# Maximum value among x, y, and z
result = max(x, y, z)
print("Maximum value among x, y, and z:", result)  # Output: 5

# Minimum value among x, y, and z
result = min(x, y, z)
print("Minimum value among x, y, and z:", result)  # Output: -4
